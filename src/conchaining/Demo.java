package conchaining;

public class Demo 
{
  public Demo()
  {
	  System.out.println("hi");
  }
  public Demo(int x)
  {
	  System.out.println(x);
  }
  public Demo(boolean x,String y)
  {
	  System.out.println(x);
	  System.out.println(y);
  }
  public Demo(String x,char y)
  {
	  System.out.println(x);
	  System.out.println(y);
  }

}
